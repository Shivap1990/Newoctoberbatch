package operators;

public class RelationalOperators {

	public static void main(String[] args) {
		int i1 = 10;
		int i2 = 20;
		System.out.println("-------Relational Operators (== < > <= >= !=)-----");
        System.out.println(i1 == i2);
        System.out.println(i1 < i2);
        System.out.println(i1 > i2);
        System.out.println(i1 <= i2);
        System.out.println(i1 >= i2);
        System.out.println(i1 != i2);
	}

}
