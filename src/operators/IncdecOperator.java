package operators;

public class IncdecOperator {

	public static void main(String[] args) {
		
		int a = 10;
        // post increment
		     a++;
       //	pre increment
		     ++a;
		     System.out.println(a);
		     
		     int b = 25;
       // post decrement
		     b--;
       // pre decrement
		     --b;
		     System.out.println(b);

	}

}
